# max and min only work with similar datatype

my_list = ['z','a','b']
print("Max String :",max(my_list))

my_list = [55,98,23]
print("Max Integer :",max(my_list))

my_list = ['z','a','b','A']
print("Min String :",min(my_list))

my_list = [55,98,23]
print("Min Integer :",min(my_list))

