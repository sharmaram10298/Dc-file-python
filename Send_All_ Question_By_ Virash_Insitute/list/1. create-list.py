"""How to create a list?
In Python programming, a list is created by placing all the items (elements) inside a
square bracket [ ],
separated by commas.
It can have any number of items and they may be of different types
(integer, float, string etc.) Hetrogenous data.
A list is a collection which is ordered and changeable. """

# empty list
my_list = []
print("Empty List :",my_list)

# list of strings
my_list = ["acgfdg","b","c"]
print("String Data Type :",my_list)

# list of integers
my_list = [1, 2, 3]
print("Integer Data Type :",my_list)

# list with mixed datatypes
my_list = [1, "Hello", 3.4]
print("Mixed Data Type :",my_list)

# nested list [multidimensional list]
my_list = [['a', 'b'] , ['c']] 
print("Multi-Dimensional List :",my_list)

