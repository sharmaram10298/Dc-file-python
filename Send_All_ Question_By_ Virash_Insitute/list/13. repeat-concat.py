# We can also use + operator to combine two lists.
# This is also called concatenation.The * operator repeats a list for the given number of times.

my_list = [1, 3, 5]

print(my_list + [9, 7, 5])
# Output: [1, 3, 5, 9, 7, 5]

print(["re","fr"] * 3)
#Output: ["re", "re", "re"]

print([4] * 3)
#Output: [4, 4, 4]
